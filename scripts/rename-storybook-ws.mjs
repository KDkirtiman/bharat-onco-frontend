import fs from "node:fs";
const sbPkg = JSON.parse(fs.readFileSync("src/apps/storybook/package.json","utf8"));
sbPkg.name = "bfd-storybook";
fs.writeFileSync("src/apps/storybook/package.json", JSON.stringify(sbPkg, null, 2));
const root = JSON.parse(fs.readFileSync("package.json","utf8"));
root.scripts.dev = "npm run storybook --workspace=bfd-storybook";
root.scripts.storybook = "npm run storybook --workspace=bfd-storybook";
root.scripts["build-storybook"] = "npm run build-storybook --workspace=bfd-storybook";
fs.writeFileSync("package.json", JSON.stringify(root, null, 2));
console.log("renamed storybook workspace");
